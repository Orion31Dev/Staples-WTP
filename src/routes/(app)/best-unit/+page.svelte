	<div class="title">Unit 3 Finishes Their Statements</div>
	<p>
		This past Wednesday, Jan 18th, Unit 3 has become the first second unit to complete all of its
		statements for the We the People: The Citizens and the Constitution competition. The achievement
		of this glorious milestone for the second consecutive year further proves the dominant legacy of
		Unit 3, which is this year's recipient of the Unit of the Year Award.
	</p>

	<p>
		“Best unit for real,” Aidan Sprouls '23 said. “The drafts really came a long way.” Talia
		Moskowitz '24 recalls the beginning of the 2022-23 school year: “I was nervous I wouldn't be
		able to join the exclusive Unit 3.” Moskowitz said. “It definitely had a compounding effect when
		Kam sent a second unit choice form.”
	</p>

	<p>
		Unit 3 has faced many trials and tribulation during the drafting process. At one point, they
		even had to read the textbook. Yet, overcoming these obstacles Unit 3 became the first second
		unit to finish all their statements. We were able to speak with Tommy Dry '24 and Ayla Nahmias
		'24 of Unit 3 about the pace with which their statements were completed. “Simply put we're just
		the best unit and that's why we work so well together.” Nahmias said. Tommy agreed with this
		opinion stating the group's “pure genius and adroitness” pushed them into success.
	</p>
